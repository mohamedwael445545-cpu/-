function showSection(id) {
  document.querySelectorAll('section').forEach(s => s.classList.remove('active'));
  document.getElementById(id + '-section').classList.add('active');
  if(id === 'prayers') loadDay();
}

function loadDay() {
  const date = document.getElementById('target-date').value;
  const container = document.getElementById('prayer-list');
  container.innerHTML = '';
  const prayers = ["الفجر", "الظهر", "العصر", "المغرب", "العشاء"];
  prayers.forEach(p => {
    const status = localStorage.getItem(`${date}-${p}`) || '❌';
    const card = document.createElement('div');
    card.className = `card ${status === '✅' ? 'done' : ''}`;
    card.innerHTML = `<span>${p}</span><button class="status-btn" onclick="toggleP('${date}','${p}')">${status}</button>`;
    container.appendChild(card);
  });
}

function toggleP(d, p) {
  const s = localStorage.getItem(`${d}-${p}`) === '✅' ? '❌' : '✅';
  localStorage.setItem(`${d}-${p}`, s);
  loadDay();
}

function loadAzkar(t) {
  const container = document.getElementById('azkar-list');
  container.innerHTML = '';
  azkarData[t].forEach((z, i) => {
    const card = document.createElement('div');
    card.className = 'card';
    card.innerHTML = `<span>${z.text}</span><button class="counter-btn" onclick="hit(this)">${z.count}</button>`;
    container.appendChild(card);
  });
}

function hit(btn) {
  let c = parseInt(btn.innerText);
  if (c > 1) { btn.innerText = c - 1; } 
  else { btn.parentElement.classList.add('done'); btn.innerText = '✅'; btn.disabled = true; }
}

showSection('prayers');
